            <h1><span>Calendário Permanente</span></h1>
            <p id="noContextMenu">O calendário permanente “Abelhas Nativas Brasileiras” surgiu como uma proposta de proporcionar informação  através da arte. As abelhas, seres belos e fascinantes, desempenham o papel vital de agentes polinizadores de grande parte de nossa flora nativa, contribuindo enormemente para a manutenção e restauração da vegetação de todos os biomas brasileiros. Ainda conhecemos muito pouco sobre a rica fauna de abelhas brasileira, que possui uma das maiores diversidades do mundo.</p><br>
            <p>A atenção geralmente é focada nas abelhas da espécie Apis mellifera, a abelha africanizada, que é exótica ao nosso território. Embora elas tenham uma importância significativa na polinização de muitas plantas, as abelhas nativas mantém uma estreita relação com nossa flora nativa há milhões de anos, sendo cruciais para sua perpetuação.</p><br>
            <p>Portanto, precisam ser protegidas epreservadas. Assim, nos concentramos nessas abelhas,especialmente naquelas sem ferrão, grupo de abelhas sociais que vêm ganhando atenção da população nos últimos anos.</p><br>
            <p>São abelhas passíveis de serem criadas em ambientes urbanos, inclusive em apartamentos. Criar colônias de abelhas colabora na preservação e propagação de muitas espécies; na polinização da flora local; e ainda traz a possibilidade de se tirar um mel fresquinho e delicioso na própria casa.</p><br>
          <h2 class="citacao">Esperamos instigar a curiosidade e o interesse nestes insetos encantadores para que nos ajude a protegê-los, colaborando para um mundo mais equilibrado.</h2><br><br>
